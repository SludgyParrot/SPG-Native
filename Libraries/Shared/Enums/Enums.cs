using System;

namespace Shared
{
    /// <summary>
    /// Defines a set of named colors for use in the application.
    /// </summary>
    /// <remarks>
    /// The <see cref="ColorRef"/> enumeration provides a set of predefined color names that can be used
    /// throughout the application to represent different colors. This can be helpful for ensuring consistency
    /// and readability when referring to colors in code.
    /// </remarks>
    public enum ColorRef
    {
        Red, 
        Green, 
        Blue, 
        Yellow, 
        Cyan, 
        Magenta, 
        Gray,
        Grey,
        White,
        Black,
        Clear
    }
}
